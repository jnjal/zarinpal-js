// ─── ZarinPal API Types ────────────────────────────────────────────
// مستندات: https://docs.zarinpal.com
// نسخه API: v4

// ────────────────────────────────────────────────────────────────────
// محیط اجرایی (Sandbox یا Production)
// ────────────────────────────────────────────────────────────────────

export type SandboxMode = boolean;

// ────────────────────────────────────────────────────────────────────
// آدرس‌های API زرین‌پال
// ────────────────────────────────────────────────────────────────────

export interface ZarinpalEndpoints {
  request: string;
  verify: string;
  startPay: string;
  unverified: string;
}

// ────────────────────────────────────────────────────────────────────
// درخواست پرداخت (Payment Request)
// ────────────────────────────────────────────────────────────────────

export interface PaymentRequestInput {
  /** شناسه merchant زرین‌پال */
  merchant_id: string;
  /** مبلغ به تومان (حداقل 1000 تومان) */
  amount: number;
  /** آدرس بازگشت پس از پرداخت */
  callback_url: string;
  /** توضیحات تراکنش (حداکثر 100 کاراکتر) */
  description: string;
  /** اطلاعات تکمیلی کاربر */
  metadata?: PaymentMetadata;
}

export interface PaymentMetadata {
  email?: string;
  mobile?: string;
}

// ────────────────────────────────────────────────────────────────────
// پاسخ درخواست پرداخت
// ────────────────────────────────────────────────────────────────────

export interface PaymentRequestResponse {
  data: {
    code: number;
    message: string;
    authority: string;
    fee: number;
  };
  errors: ZarinpalError[];
}

// ────────────────────────────────────────────────────────────────────
// وریفای پرداخت (Payment Verify)
// ────────────────────────────────────────────────────────────────────

export interface PaymentVerifyInput {
  /** شناسه merchant زرین‌پال */
  merchant_id: string;
  /** مبلغ به تومان (باید با مبلغ درخواست برابر باشد) */
  amount: number;
  /** کد authority از مرحله قبل */
  authority: string;
}

export interface PaymentVerifyResponse {
  data: {
    code: number;
    message: string;
    card_hash: string;
    card_pan: string;
    ref_id: number;
    fee_type: string;
    fee: number;
  };
  errors: ZarinpalError[];
}

// ────────────────────────────────────────────────────────────────────
// دریافت لیست پرداخت‌های وریفای نشده
// ────────────────────────────────────────────────────────────────────

export interface UnverifiedInput {
  merchant_id: string;
}

export interface UnverifiedResponse {
  data: {
    authorities: Array<{
      amount: number;
      authority: string;
    }>;
  };
  errors: ZarinpalError[];
}

// ────────────────────────────────────────────────────────────────────
// خطای زرین‌پال
// ────────────────────────────────────────────────────────────────────

export interface ZarinpalError {
  code: number;
  message: string;
}

// ────────────────────────────────────────────────────────────────────
// تنظیمات کلاینت
// ────────────────────────────────────────────────────────────────────

export interface ZarinpalConfig {
  /** شناسه merchant زرین‌پال */
  merchantId: string;
  /** آیا از sandbox استفاده شود؟ (پیش‌فرض: false) */
  sandbox?: boolean;
  /** تایم‌اوت درخواست‌ها به میلی‌ثانیه (پیش‌فرض: 15000) */
  timeout?: number;
}

